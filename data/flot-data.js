//Flot Moving Line Chart

$(function() {




    var container = $("#flot-line-chart-moving");

    // Determine how many data points to keep based on the placeholder's initial size;
    // this gives us a nice high-res plot while avoiding more than one point per pixel.

    var maximum = container.outerWidth() / 2 || 300;

    //

    var data = [];

    var getRandomData = function(){
        return getRandomDataBs(lineData[0].data);
    }

    $('#changeType').click(function(){
        var changeTypeValue = $('#changeType').is(':checked')
        if(changeTypeValue === true){
            //日间
            clearInterval(updateRandom, 40);
            data = [];
            var getRandomData = function(){
                return getRandomDataBs(lineData[0].data);
            }
            series = [{
                data: getRandomData(),
                lines: {
                    fill: true
                }
            }];
            setInterval(updateRandom, 40);
        }
        else{
            //月间
            clearInterval(updateRandom, 40);
            data = [];
            var getRandomData = function(){
                return getRandomDataBs(lineData[1].data);
            }
            series = [{
                data: getRandomData(),
                lines: {
                    fill: true
                }
            }];
            setInterval(updateRandom, 40);
        }
    });


    function getRandomDataBs(src_data) {

        var serverData = src_data;
        //舍弃上一个数据
        if (data.length) {
            data = data.slice(1);
            serverData = serverData.slice(1);
        }

        //若数据不足，则获取数据
        while (data.length < maximum && serverData.length > data.length) {
            data.push(serverData[data.length][1]);
        }

        // zip the generated y values with the x values

        var res = [];
        for (var i = 0; i < data.length; ++i) {
            res.push([i, data[i]])
        }

        console.log(res)
        return res;

    }

    //

    series = [{
        data: getRandomData(),
        lines: {
            fill: true
        }
    }];

    //

    var plot = $.plot(container, series, {
        grid: {
            borderWidth: 1,
            minBorderMargin: 20,
            labelMargin: 10,
            backgroundColor: {
                colors: ["#fff", "#e4f4f4"]
            },
            margin: {
                top: 8,
                bottom: 20,
                left: 20,
            },
            markings: function(axes) {
                var markings = [];
                var xaxis = axes.xaxis;
                for (var x = Math.floor(xaxis.min); x < xaxis.max; x += xaxis.tickSize * 2) {
                    markings.push({
                        xaxis: {
                            from: x,
                            to: x + xaxis.tickSize
                        },
                        color: "rgba(232, 232, 255, 0.2)"
                    });
                }
                return markings;
            }
        },
        xaxis: {
            tickFormatter: function() {
                return "";
            }
        },
        yaxis: {
            min: 0,
            max: 110
        },
        legend: {
            show: true
        }
    });

    // Update the random dataset at 25FPS for a smoothly-animating chart

    function updateRandom() {
        series[0].data = getRandomData();
        console.log(series[0].data)
        plot.setData(series);
        plot.draw();
    }
    setInterval(updateRandom, 40);

});